
package remote_interfaces.functionality;


public enum FunctionalityType {

	INDEFINIBLE,
	DUMMY,
	PRESENCE,
	DUMMYPRESENCE,
	LAURA;
	
	public static FunctionalityType convert(int i)
	{
		try 
		{
			return values()[i];
		} 
		catch ( ArrayIndexOutOfBoundsException e ) 
		{
			return INDEFINIBLE;
		}
	}	
	
}
