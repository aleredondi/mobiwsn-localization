
package client_applications.parking;


public class Park 
{
	private String name;
	private double price;
	private int totPark;
	private int availablePark;
	
	public Park( String n, double p, int tot, int free)
	{
		this.name=n;
		this.price=p;
		this.totPark=tot;
		this.availablePark=free;
	}
	
	public void setName(String n)
	{
		this.name=n;
	}
	
	public void setPrice(double n)
	{
		this.price=n;
	}
	
	public void setTotPark(int n)
	{
		this.totPark=n;
	}
	
	public void setAvailablePark(int n)
	{
		this.availablePark=n;
	}
	
	
	public String getName()
	{
		return this.name;
	}
	
	public double getPrice()
	{
		return this.price;
	}
	
	public int getTotPark()
	{
		return this.totPark;
	}
	
	public int getAvailablePark()
	{
		return this.availablePark;
	}
	
	
}
